/* global describe, it */
'use strict';

describe('LinkedList', function () {
    describe('#addFirst', function () {
        it('should add an element as the first in the list.');
    });
    describe('#addLast', function () {
        it('should add an element as the last in the list.');
    });
    describe('#removeFirst', function () {
        it('should remove the first element in the list.');
        it('should throw if there are no elements in the list.');
    });
    describe('#removeLast', function () {
        it('should remove the last element in the list.');
        it('should throw if there are no elements in the list.');
    });
});
